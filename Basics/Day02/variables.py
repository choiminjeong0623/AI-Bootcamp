## 변수는 데이터를 저장하는 이름표이다.
name = "민정"         ## 문자는 따옴표 사용
age = 31              ## 숫자는 따옴표 사용하지 않음
height = 166.0         ## 실수(float)도 따옴표 사용하지 않음
student=False          ## 불리언(Boolean)도 따옴표 사용하지 않음
print(name)
print(age)
print(height,student)

# input() 함수는 사용자로부터 입력을 받을 때 사용한다.
name=input("이름을 입력하세요 : ")
print(name)

